package com.hegel.reflect;

public class User {

    private int id;

    private String name;

    private String login;

    private String password;

    private boolean isTxtEnable;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getLogin() {
        return login;
    }

    public String getPassword() {
        return password;
    }

    public boolean isTxtEnable() {
        return isTxtEnable;
    }
}
